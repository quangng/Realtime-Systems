static void ct_do_row() { // row fight the power
	int i;
	int newfreq;
	ct_channel* chan;
	ct_event* event;

	for(i=0;i<4;i++) {
		chan = song->channels[i];
		event = chan->events[song->row];
		newfreq = note_2_freq[event->note]<<16;

		if(event->note != 0) {
			chan->drum_sample_pos = 0;
			if(i==2) newfreq -= 24<<16;
/*			if(event->fx == '3') {
				chan->freq += chan->delta_freq;
				chan->tone_porta_freq = newfreq;
				chan->tone_porta_modifier = newfreq - chan->freq;
				if(chan->tone_porta_modifier<0) chan->tone_porta_modifier=-chan->tone_porta_modifier;
			}
			else*/
				chan->freq = newfreq;

			chan->delta_freq = 0;
			chan->inst = event->inst;
			chan->vol = event->vol&0xFF;
		} else {
			if(event->vol < 255) chan->vol = event->vol;
			if(event->inst > 0) chan->inst = event->inst;
		}

		switch(event->fx) {
		case '0':
		case '1':
		case '2':
		case '3':
		case '4':
		case 'A':
		case 'C':
			chan->fx = event->fx;
			if(event->param || !chan->param) chan->param = event->param;
			break;
		case 'D':
			song->loop=1;
			chan->fx = -1;
			break;
		case 'F':
			song->speed = event->param & 0xF;
		default:
			chan->fx = -1;
		}

		if(chan->vibrato_pos && event->fx != '4') {
			chan->delta_freq = 0;
		}
		if(event->fx != '4') {
			chan->vibrato_pos = 0;
		}

	}
}

static void ct_do_tick() {
	int i;
	ct_channel* chan;

	for(i=0;i<4;i++) {
		chan = song->channels[i];

			if(chan->fx=='0') {
				switch(song->tick % 3) {
				case 1:
					chan->delta_freq = (chan->param & 0xF0)<<12;
					break;
				case 2:
					chan->delta_freq = (chan->param & 0x0F)<<16;
					break;
				default:
					chan->delta_freq = 0;
					break;
				}
			}
			else if(chan->fx=='1') {
				chan->delta_freq += (2 * 65536 * chan->param)/100;
			}
			else if(chan->fx=='2') {
				chan->delta_freq -= (2 * 65536 * chan->param)/100;
			}
			else if(chan->fx=='3') {
/*				float tone_porta_speed = chan->tone_porta_modifier*chan->param/110.0;
				if(chan->freq < (chan->tone_porta_freq - tone_porta_speed) ) {
					chan->freq += tone_porta_speed;
				}
				else if(chan->freq > (chan->tone_porta_freq + tone_porta_speed) ) {
					chan->freq -= tone_porta_speed;
				}
				else {
					chan->freq = chan->tone_porta_freq;
				}*/
			}
			else if(chan->fx=='4') {
				chan->delta_freq = ((chan->param & 0x0F) * sin16(chan->vibrato_pos))/8;
				chan->vibrato_pos += ((chan->param & 0xF0)<<12) / 60.0;
			}
			else if(chan->fx=='A') {
				chan->vol += (chan->param & 0xF0) >>2;
				chan->vol -= (chan->param & 0x0F) <<2;
				if(chan->vol < 0) chan->vol = 0;
				if(chan->vol > 1024) chan->vol = 1024;
			}

	}
}

// these waveforms should be defined on [0.0,1.0)
static int32_t saw(uint16_t phase) {
	return phase-(1<<15);
}

static int32_t squ1(uint16_t phase) {
	return (phase > (1<<15))?22938:-22938;
}

static int32_t squ2(uint16_t phase) {
	return (phase > (1<<14))?22938:-22938;
}

static int32_t squ3(uint16_t phase) {
	return (phase > (1<<13))?22938:-22938;
}

static int32_t squ4(uint16_t phase) {
	return (phase > (1<<12))?22938:-22938;
}

static int32_t tri(uint16_t phase) {
	return sin16(phase);
}


static int32_t (*instruments[])(uint16_t) = { saw, squ1, squ2, squ3, squ4, tri };


int16_t GetCTSample() {
	int sample=0;
	ct_channel* chan;
	int i, inst;

	if(song->sample==0)	{
		ct_do_tick();
		if(song->tick==0)	
			ct_do_row();
	}

	for(i=0;i<3;i++) {
		chan = song->channels[i];
		if(chan->vol > chan->real_vol) chan->real_vol++;
		else if(chan->vol < chan->real_vol) chan->real_vol--;
		chan->real_vol = chan->vol;
		inst = chan->inst;
		if(chan->fx == 'C' && song->tick % 3) inst = ((song->tick % 3) == 1)? (chan->param & 0xF0 >> 4) : (chan->param & 0x0F);
		if(i==2) { // bass: swap tri and saw
			if(inst == 0) inst = 5;
			else if(inst == 5) inst = 0;
		}

		sample += instruments[inst](chan->phase) * chan->real_vol / (255*4);
		chan->phase += phasestep(chan->freq + chan->delta_freq);

//		sample += 8191.0 * instruments[inst](chan->phase) * ((float)(chan->real_vol) / 255.0);
//		chan->phase += 261.625565 * powf(FREQ_BASE,chan->freq + chan->delta_freq) / 44100.0;
//		chan->phase = fmodf(chan->phase,1);
	}

	chan = song->channels[3];
	i = chan->freq>>16;
	if(chan->drum_sample_pos < drum_lengths[i]) {
			sample += drum_samples[i][chan->drum_sample_pos++] / 4;
	}

	song->sample++;

	// advance positions
	if(song->sample == 882) {
		song->tick++;
		song->sample = 0;

	}
	if(song->tick == song->speed) {
		song->row++;
		if(song->loop == 1) song->row = 64;
		song->tick = 0;
	}
	if(song->row == 64) {
		song->row = 0;
		song->loop = 0;
	}

	return sample;
}





static int32_t phasestep(int32_t val)
{
	static const int32_t table[13]=
	{
		815363806,
		863847861,
		915214928,
		969636440,
		1027294023,
		1088380104,
		1153098553,
		1221665361,
		1294309364,
		1371273004,
		1452813140,
		1539201904,
		1630727612,
	};

	int shift=(val/65536)/12;
	int num=(val/65536)%12;
	int denom=val&0xffff;

	if(num<0) { num+=12; shift-=1; }

	int val1=table[num]>>(21-shift);
	int val2=table[num]>>(21-shift);

	return val1+(((val2-val1)*(denom>>8))>>8);
}

