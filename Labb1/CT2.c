typedef struct ct_event {
	uint8_t note;
	uint8_t inst;
	uint8_t vol;
	uint8_t fx;
	uint8_t param;
} ct_event;

typedef struct ct_channel {
	int drums;
	ct_event* events[64];
	int fx;
	int param;
	int vol;
	int real_vol; // slow-response volume for avoiding clicks
	int inst;
	int drum_sample_pos; 
	uint16_t vibrato_pos;
	float tone_porta_freq;
	float tone_porta_modifier; // speed modifier for toneporta, dependent on frequency distance
	int32_t freq;
	int32_t delta_freq;
	uint16_t phase;
} ct_channel;


typedef struct ct_song {
	ct_channel* channels[4];
	int row;
	int tick; // current tick within the row
	int sample; // current sample within the tick
	int speed; // ticks per row
	int loop; // 1: loop to beginning on next row
} ct_song;


