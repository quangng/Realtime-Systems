static ct_event* alloc_event() {
	static ct_event eventbuf[64*4];
	static int n=0;
	ct_event* event = &eventbuf[n++];
	event->note = event->inst = event->param = 0;
	event->fx = -1;
	event->vol = 0xFF;
	return event;
}

