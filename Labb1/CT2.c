static ct_song* alloc_song() {
	int i;
	static ct_song songbuf;
	ct_song* song = &songbuf;
	for(i=0;i<4;i++)
		song->channels[i] = alloc_channel();
	song->channels[3]->drums = 1;
	song->row = song->tick = song->sample = 0;
	song->loop = 0;
	song->speed = 7;
	return song;
}

static ct_song* song_load() {
	int pos=0, chan=0, event=0;
	ct_song* song = alloc_song();
	for(;chan<4;chan++) {
		for(event=0;event<64;event++) {
			*song->channels[chan]->events[event]=testsong[chan][event];
		}
		pos+=8;
	}
	return song;
}




